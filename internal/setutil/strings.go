package setutil

import "sort"

// SortedStrings returns a stable sorted slice from a string set.
func SortedStrings(values map[string]struct{}) []string {
	if len(values) == 0 {
		return nil
	}
	out := make([]string, 0, len(values))
	for value := range values {
		out = append(out, value)
	}
	sort.Strings(out)
	return out
}
